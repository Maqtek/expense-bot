DEFAULT_CATEGORY = "Без категории"


def categorize(name: str) -> str:
    """Определяет категорию товара по его названию.

    Ищет все ключевые слова, встречающиеся в названии,
    и возвращает категорию с самым длинным совпадением.
    Если ничего не нашлось — возвращает 'Без категории'.
    """
    name_low = name.lower()
    best_category = DEFAULT_CATEGORY
    best_len = 0

    for category, keywords in CATEGORIES.items():
        for kw in keywords:
            if kw in name_low and len(kw) > best_len:
                best_category = category
                best_len = len(kw)   # запоминаем длину самого длинного совпадения

    return best_category


